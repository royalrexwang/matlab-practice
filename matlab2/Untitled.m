th=0:pi/18:2*pi;
t=0:1/36:1;
y1=[0,0];
y2=[0,0];
y3=[0,0];
x1=zeros(37,2);
x2=zeros(37,2);
x3=zeros(37,2);
for i=1:1:37
xx1=fsolve(@myfun1,y1,optimset,th(i));
x1(i,1)=xx1(1,1);
x1(i,2)=xx1(1,2);
y1=xx1;
xx2=fsolve(@myfun2,y2,optimset,xx1,th(i));
x2(i,1)=xx2(1,1);
x2(i,2)=xx2(1,2);
y2=xx2;
xx3=fsolve(@myfun3,y3,optimset,xx2,xx1,th(i));
x3(i,1)=xx3(1,1);
x3(i,2)=xx3(1,2);
y3=xx3;
clc;
end
subplot(2,3,1)
plot(t,x1(:,1));
title('t-\phi2曲线');
xlabel('t')
ylabel('\phi2')
subplot(2,3,4)
plot(t,x1(:,2));
title('t-xc曲线')
xlabel('t')
ylabel('Xc')
subplot(2,3,2)
plot(t,x2(:,1));
title('t-\omega2曲线')
xlabel('t')
ylabel('\omega2')
subplot(2,3,5)
plot(t,x2(:,2));
title('t-vc曲线')
xlabel('t')
ylabel('Vc')
subplot(2,3,3)
plot(t,x3(:,1))
title('t-\alpha2曲线')
xlabel('t')
ylabel('\alpha2')
subplot(2,3,6)
plot(t,x3(:,2));
title('t-ac曲线')
xlabel('t')
ylabel('ac')